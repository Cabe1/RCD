                    <h3>Who are we?</h3>
                    <p>
                        Radford Child Development, Inc. is a non-profit organization determined to develop a quality, certified child care facility in Radford. Based on our needs assessment, we concluded that many members of the Radford community are struggling to find a decent, nationally certified child care for their children. 
                    </p>
                    <h3>Benefits of a Radford Child Development Center</h3>
                    <ul>
                        <li>A Radford resource available to all Radford area employers.</li>
                        <li>Internship opportunities for RU early education and other disciplines.</li>
                        <li>Highest quality NAEY-C childcare in Radford to serve our communities's children and families.</li>
                    </ul>
                    <h3>We are 501c3 pending</h3>
                    <p>
                        Although the 501c3 application process can take several months, we anticipate IRS approval for this nonprofit.
                    </p>
                    <h3>What does this mean?</h3>
                    <p>
                        Radford Child Development, Inc. is a non-profit organization incorporated in the state of Virginia. Nonprofit organizations are given 27 months from the date of incorporation to fill out IRS form 1023 to apply for tax-exempt status. We completed and submitted our 1023 form to IRS.
                    </p>
                    <h3>Why does this matter to you?</h3>
                    <p>
                        Once 1023 approved, it is inclusive of all donations taken in from the incorporation date to the date of 501c3 approval. Basically, this means that your donations are tax-deductible. 
                    </p>
